package GeometryOperation;

public class GeometricOper {



    public double areaOfCircle(int radiusR) {
          return  3.14 * (radiusR * radiusR);

        }
    public int areaOfSquare(int side) {
        return  side * side;

    }
    public int areaOfRectangle(int sideA, int sideB ) {
        return  sideA * sideB;

    }
}
